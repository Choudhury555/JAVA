package DATA_TYPE;

public class Data_Type_Practice_1 {
    public static void main(String[] args) {
        int eight = 017;//anything starting with "0" is an octal number
        int sixteen = 0x10;//anything starting with "0x" is a hexadecimal number

        System.out.println(eight);
        System.out.println(sixteen);

        int big = 0xAB;
        System.out.println(big);
    }
}
